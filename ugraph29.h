// Binary search for property of `c` in sorted `table`
static enum ugraph29_prop ugraph29_lookup_prop(struct ugraph29_table *table, int ntable, uint32_t c) {
    int lo = 0;
    int hi = ntable - 1;

    // Fast path for printable ASCII range
    if (c >= 0x20 && c <= 0x7e) {
        return 0;
    }

    while (lo <= hi) {
        int i = (lo + hi) / 2;
        if (c < table[i].range_start) {
            hi = i - 1;
        } else if (c > table[i].range_end) {
            lo = i + 1;
        } else {
            return table[i].prop;
        }
    }
    return 0;
}

// Get next grapheme cluster length of string `cps` starting at offset `start`
ptrdiff_t ugraph29_get_grapheme_length(struct ugraph29_state *s, uint32_t *cps, ptrdiff_t ncps, ptrdiff_t start) {
    ptrdiff_t i;
    enum ugraph29_prop a, b;

    for (i = start; i + 1 < ncps; i++) {
        uint32_t ac = cps[i];
        uint32_t bc = cps[i + 1];

        if (i == start) {
            // Fast path for printable ASCII range
            if (ac >= 0x20 && ac <= 0x7e && bc >= 0x20 && bc <= 0x7e) {
                return 1;
            }

            // Lookup props of first codepoint
            a = 0
                | ugraph29_lookup_prop(ugraph29_table_grapheme_break, ugraph29_table_grapheme_break_count, ac)
                | ugraph29_lookup_prop(ugraph29_table_incb, ugraph29_table_incb_count, ac)
                | ugraph29_lookup_prop(ugraph29_table_ext_picto, ugraph29_table_ext_picto_count, ac);
        } else {
            a = b;
        }

        // Lookup props of second codepoint
        b = 0
            | ugraph29_lookup_prop(ugraph29_table_grapheme_break, ugraph29_table_grapheme_break_count, bc)
            | ugraph29_lookup_prop(ugraph29_table_incb, ugraph29_table_incb_count, bc)
            | ugraph29_lookup_prop(ugraph29_table_ext_picto, ugraph29_table_ext_picto_count, bc);

        // Run state machine for GB9c
        //
        // Do not break within certain combinations with Indic_Conjunct_Break
        // (InCB)=Linker.
        //
        // GB9c: \p{InCB=Consonant} [ \p{InCB=Extend} \p{InCB=Linker} ]* \p{InCB=Linker} [ \p{InCB=Extend} \p{InCB=Linker} ]* <nobreak> \p{InCB=Consonant}
        if (s->gb9c == 0 && (a & UGRAPH29_PROP_INCB_CONSONANT)) {
            s->gb9c = 1;
        } else if (s->gb9c == 1 && (a & UGRAPH29_PROP_INCB_EXTEND)) {
            s->gb9c = 1;
        } else if (s->gb9c == 1 && (a & UGRAPH29_PROP_INCB_LINKER)) {
            s->gb9c = 2;
        } else if (s->gb9c == 2 && (a & (UGRAPH29_PROP_INCB_EXTEND | UGRAPH29_PROP_INCB_LINKER))) {
            s->gb9c = 2;
        } else if (s->gb9c) {
            s->gb9c = (a & UGRAPH29_PROP_INCB_CONSONANT) ? 1 : 0;
        }

        // Run state machine for GB11
        //
        // Do not break within emoji modifier sequences or emoji zwj sequences.
        //
        // GB11: \p{Extended_Pictographic} Extend* ZWJ <nobreak> \p{Extended_Pictographic}
        if (s->gb11 == 0 && (a & UGRAPH29_PROP_EXT_PICTO)) {
            s->gb11 = 1;
        } else if ((s->gb11 == 1 || s->gb11 == 2) && (a & UGRAPH29_PROP_GRAPHEME_BREAK_EXTEND)) {
            s->gb11 = 2;
        } else if ((s->gb11 == 1 || s->gb11 == 2) && (a & UGRAPH29_PROP_GRAPHEME_BREAK_ZWJ)) {
            s->gb11 = 3;
        } else if (s->gb11) {
            s->gb11 = (a & UGRAPH29_PROP_EXT_PICTO) ? 1 : 0;
        }

        // Run state machine for GB12 and GB13
        //
        // Do not break within emoji flag sequences. That is, do not break
        // between regional indicator (RI) symbols if there is an odd number of
        // RI characters before the <break> point.
        //
        // GB12: sot   (RI RI)* RI <nobreak> RI
        // GB13: [^RI] (RI RI)* RI <nobreak> RI
        if (a & UGRAPH29_PROP_GRAPHEME_BREAK_REGIONAL_INDICATOR) {
            s->gb12_13 += 1;
        } else {
            s->gb12_13 = 0;
        }

        // Apply UAX-29 rules
        if ((a & UGRAPH29_PROP_GRAPHEME_BREAK_CR) && (b & UGRAPH29_PROP_GRAPHEME_BREAK_LF)) {
            // Do not break between a CR and LF.
            //
            // GB3: CR <nobreak> LF
            continue;
        } else if ((a | b) & (UGRAPH29_PROP_GRAPHEME_BREAK_CONTROL | UGRAPH29_PROP_GRAPHEME_BREAK_CR | UGRAPH29_PROP_GRAPHEME_BREAK_LF)) {
            // Otherwise, break before and after controls.
            //
            // GB4: (Control | CR | LF) <break>
            // GB5: <break> (Control | CR | LF)
            return (i - start) + 1;
        } else if ((a & UGRAPH29_PROP_GRAPHEME_BREAK_L)
            && (b & (UGRAPH29_PROP_GRAPHEME_BREAK_L | UGRAPH29_PROP_GRAPHEME_BREAK_V | UGRAPH29_PROP_GRAPHEME_BREAK_LV | UGRAPH29_PROP_GRAPHEME_BREAK_LVT))
        ) {
            // Do not break Hangul syllable or other conjoining sequences.
            //
            // GB6: L <nobreak> (L | V | LV | LVT)
            continue;
        } else if ((a & (UGRAPH29_PROP_GRAPHEME_BREAK_LV | UGRAPH29_PROP_GRAPHEME_BREAK_V))
            && (b & (UGRAPH29_PROP_GRAPHEME_BREAK_V | UGRAPH29_PROP_GRAPHEME_BREAK_T))
        ) {
            // GB7: (LV | V) <nobreak> (V | T)
            continue;
        } else if ((a & (UGRAPH29_PROP_GRAPHEME_BREAK_LVT | UGRAPH29_PROP_GRAPHEME_BREAK_T))
            && (b & UGRAPH29_PROP_GRAPHEME_BREAK_T)
        ) {
            // GB8: (LVT | T) <nobreak> T
            continue;
        } else if (b & (UGRAPH29_PROP_GRAPHEME_BREAK_EXTEND | UGRAPH29_PROP_GRAPHEME_BREAK_ZWJ)) {
            // Do not break before extending characters or ZWJ.
            //
            // GB9: <nobreak> (Extend | ZWJ)
            continue;
        } else if (b & UGRAPH29_PROP_GRAPHEME_BREAK_SPACINGMARK) {
            // Do not break before SpacingMarks, or after Prepend characters.
            //
            // GB9a: <nobreak> SpacingMark
            continue;
        } else if (a & UGRAPH29_PROP_GRAPHEME_BREAK_PREPEND) {
            /* GB9b: Prepend <nobreak> */
            continue;
        } else if ((s->gb9c >= 2 && s->gb9c <= 4) && (b & UGRAPH29_PROP_INCB_CONSONANT)) {
            // Do not break within certain combinations with
            // Indic_Conjunct_Break (InCB)=Linker.
            //
            // GB9c: \p{InCB=Consonant} [ \p{InCB=Extend} \p{InCB=Linker} ]* \p{InCB=Linker} [ \p{InCB=Extend} \p{InCB=Linker} ]* <nobreak> \p{InCB=Consonant}
            s->gb9c = 0;
            continue;
        } else if (s->gb11 == 3 && (b & UGRAPH29_PROP_EXT_PICTO)) {
            // Do not break within emoji modifier sequences or emoji zwj
            // sequences.
            //
            // GB11: \p{Extended_Pictographic} Extend* ZWJ <nobreak> \p{Extended_Pictographic}
            s->gb11 = 0;
            continue;
        } else if (((s->gb12_13 % 2) != 0) && (b & UGRAPH29_PROP_GRAPHEME_BREAK_REGIONAL_INDICATOR)) {
            // Do not break within emoji flag sequences. That is, do not break
            // between regional indicator (RI) symbols if there is an odd number
            // of RI characters before the <break> point.
            //
            // GB12: sot   (RI RI)* RI <nobreak> RI
            // GB13: [^RI] (RI RI)* RI <nobreak> RI
            continue;
        } else {
            // Otherwise, break everywhere.
            //
            // GB999: Any <break> Any
            return (i - start) + 1;
        }
    }

    // Break at the start and end of text, unless the text is empty.
    //
    // GB2: Any <break> eot
    return ncps - start;
}

